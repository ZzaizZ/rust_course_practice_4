/// Отражает изображение `image` в формате RGBA8 по горизонтали и/или вертикали.
///
/// Операции применяются последовательно: сначала горизонтальное отражение
/// (`flip_h`), затем вертикальное (`flip_v`). Каждая операция выполняется
/// попарным обменом пикселей in-place, без выделения дополнительной памяти.
///
/// # Аргументы
///
/// * `image`  — пиксельные данные в формате RGBA8 (4 байта на пиксель, построчно).
/// * `width`  — ширина изображения в пикселях.
/// * `height` — высота изображения в пикселях.
/// * `flip_v` — отражение по вертикали (верх ↔ низ).
/// * `flip_h` — отражение по горизонтали (лево ↔ право).
pub(crate) fn mirror(image: &mut [u8], width: u32, height: u32, flip_v: bool, flip_h: bool) {
    let w = width as usize;
    let h = height as usize;

    if flip_h {
        mirror_half(image, h, w / 2, |x, y| (y * w + x, y * w + (w - 1 - x)));
    }

    if flip_v {
        mirror_half(image, h / 2, w, |x, y| (y * w + x, (h - 1 - y) * w + x));
    }
}

/// Попарно обменивает пиксели (все 4 байта RGBA) по позициям `a` и `b`.
fn swap_pixels(image: &mut [u8], a: usize, b: usize) {
    for i in 0..4 {
        image.swap(a * 4 + i, b * 4 + i);
    }
}

/// Обходит прямоугольник `rows × cols` пикселей и для каждой позиции `(x, y)`
/// вычисляет пару индексов через `indices_to_swap`, после чего меняет
/// соответствующие пиксели местами.
///
/// Используется как обобщённый примитив как для горизонтального,
/// так и для вертикального отражения.
fn mirror_half(
    image: &mut [u8],
    rows: usize,
    cols: usize,
    indices_to_swap: impl Fn(usize, usize) -> (usize, usize),
) {
    for y in 0..rows {
        for x in 0..cols {
            let (a, b) = indices_to_swap(x, y);
            swap_pixels(image, a, b);
        }
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_horizontal_mirror() {
        let mut image = vec![
            1, 2, 3, 255, 4, 5, 6, 255, 7, 8, 9, 255, 10, 11, 12, 255, 13, 14, 15, 255, 16, 17, 18,
            255, 19, 20, 21, 255, 22, 23, 24, 255, 25, 26, 27, 255,
        ];

        mirror(&mut image, 3, 3, false, true); // горизонтальное отражение
        assert_eq!(
            image,
            vec![
                // row0: (7..9) + (4..6) + (1..3)
                7, 8, 9, 255, 4, 5, 6, 255, 1, 2, 3, 255,
                // row1: (16..18) + (13..15) + (10..12)
                16, 17, 18, 255, 13, 14, 15, 255, 10, 11, 12, 255,
                // row2: (25..27) + (22..24) + (19..21)
                25, 26, 27, 255, 22, 23, 24, 255, 19, 20, 21, 255,
            ]
        );
    }

    #[test]
    fn test_vertical_mirror() {
        let mut image = vec![
            1, 2, 3, 255, 4, 5, 6, 255, 7, 8, 9, 255, 10, 11, 12, 255, 13, 14, 15, 255, 16, 17, 18,
            255, 19, 20, 21, 255, 22, 23, 24, 255, 25, 26, 27, 255,
        ];

        mirror(&mut image, 3, 3, true, false); // вертикальное отражение
        assert_eq!(
            image,
            vec![
                // row0: (19..21) + (22..24) + (25..27)
                19, 20, 21, 255, 22, 23, 24, 255, 25, 26, 27, 255,
                // row1: (10..12) + (13..15) + (16..18)
                10, 11, 12, 255, 13, 14, 15, 255, 16, 17, 18, 255,
                // row2: (1..3) + (4..6) + (7..9)
                1, 2, 3, 255, 4, 5, 6, 255, 7, 8, 9, 255,
            ]
        );
    }
}
